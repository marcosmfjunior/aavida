<p>
Em 19 de abril de 1979 o Clube das Acácias de Divinópolis - formado pelas esposas dos maçons da Loja Maçônica Estrela do Oeste de Minas, 
após receber o pedido de ajuda por parte de uma mãe de uma criança surda e verificar o grande número de surdos em Divinópolis, sem receber 
nenhuma assistência ou atendimento especializado, se sensibilizou com a situação e, contando com total apoio e proteção da referida Loja Maçônica, 
fundou a Escola para surdos AAVIDA – Assistência Audio-Visual Para Deficientes Auditivos.
</p>
<p>
Inicialmente foi instalada no anexo à sede do Clube das Acácias de Divinópolis, e em 1986 foi firmado um convênio com o Estado e a escola passou a funcionar como classe anexa à Escola Estadual Helena Antipoff.
Sendo que em 25/06/1987 foi publicado no “Minas Gerais” a autorização de funcionamento da escola para o ensino de 1°. Grau (1° a 4° série).   
Em 1988 foi extinto o convênio com o Estado e a escola passou a funcionar independente.
</p>
<p>No ano de 1994, após verificada uma grande demanda para a Educação Infantil foi instituído e registrado na SER – Divinópolis o Pré-Escolar.
Até o final de 1999 os alunos da Escola ao concluírem a 4ª série eram encaminhados para a escola regular, para continuarem seus estudos.</p>
</div>
<div class="col-md-6">
<p>Após vários estudos, avaliações e ampla observação, foi constatado que o aluno surdo necessita de um tempo maior para construir a língua escrita e o salto qualitativo,
 neste sentido, acontece exatamente, na idade em ele está “fechando” os anos iniciais. 
 Foi concluído que seria necessário estender sua permanência na Escola para poder ingressar na escola comum com maior eficiência. 
 Então, a partir de 2000, começou a ser ministrado na Escola, de forma gradativa, o Ensino Fundamental de 5ª a 8ª série.
</p>
<p>
Até 31/12/1995 a Escola foi mantida pelo Clube das Acácias de Divinópolis e a partir de 1° de janeiro de 1996  
passou a ser mantida pela Sociedade Educacional e Beneficente Estrela do Oeste de Minas a qual está sujeita as suas atividades.
</p>	
